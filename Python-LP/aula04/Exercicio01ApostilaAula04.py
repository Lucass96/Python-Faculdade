#FOR
#a) sequencia de prints com inteiros de 3 ate 12, com incluso 12
for i in range(3, 13, 1):
    print(i)

#b) inteiros de 0 ate 9, excluindo 9, com passo de 2
for i in range(0, 9, 2):
    print(i)


# while
#a) sequencia de prints com inteiros de 3 ate 12, com incluso 12
i = 3
while (i < 13):
    print(i)
    i += 1

#b) inteiros de 0 ate 9, excluindo 9, com passo de 2
i = 0
while (i < 9):
    print(i)
    i += 2