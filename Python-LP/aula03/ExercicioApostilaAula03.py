# Expressoes booleanas
#a) somatorio de 2 com 2 e menor do que 4
print(2 + 2 < 4)

#b) valor 7 // 3 e igual a 1 + 1
print(7 // 3 == 1 + 1)

#c) soma de 3 elevado ao quadrado com 4 elevado ao quadrado e igual a 25
print(3 ** 2 + 4 ** 2 == 25)

#d) soma de 2, 4 e 6 e maior do que 12
print(2 + 4 + 6 > 12)

#e) 1387 e divisivel por 19
print(1387 / 19 == 0)

#f) 31 e par
print(31 / 2 == 0)

