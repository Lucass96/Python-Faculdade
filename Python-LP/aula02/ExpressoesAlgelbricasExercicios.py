#a) somatorio dos 5 primeiros numeros inteiros e positivos
soma = 1 + 2 + 3 + 4 + 5
print('A soma dos valores e {}.'.format(soma))

#b) media entre 23, 19 e 31
media = (23 + 19 + 31) / 3
print('A media dos valores e %.2f.'% media)

#c) numero de vezes que 73 cabe em 403
n1 = 403 // 73
print('O numero 73 cabe {} vezes dentro de 403.'.format(n1))

#d) a sobra quando 403 e dividido por 73
n1 = 403 % 73
print('A sobra da divisao e {}. '.format(n1))

#e) 2 elevado a 10 potencia
n1 = 2 ** 10
print('O valor e {}.'.format(n1))

#f) o valor absoluto da diferenca entre 54 e 57
n1 = abs(54 - 57)
print('O valor absoluto e {}.'.format(n1))

#g) o menor valor entre 34, 29 e 31
n1 = min(34, 29, 31)
print('O menor valor e {}.'.format(n1))